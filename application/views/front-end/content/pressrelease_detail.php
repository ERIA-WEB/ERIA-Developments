                    <p>
                        A webinar dialogue on Realizing Smart Cities was jointly hosted by the Smart Cities Network and Economic
                        Research Institute for ASEAN and East Asia (ERIA). The dialogue was moderated by Kok-Chin (KC) Tay,
                        Chairman
                        of the Smart Cities Network based in Singapore. Dr. Venkatachalam Anbumozhi, Senior Economist and the
                        Principle Investigator of the ERIA study on Realizing Smart Cities, explained the drivers of Smart Cities,
                        and provided an update on the research findings to date and ways to launch smart cities in ASEAN and
                        beyond.
                        138 participants representing policy making, business, academia and the public participated in this
                        virtual
                        dialogue, and shared their perspectives on smart cities.
                    </p>
                    <p>
                        In his opening remarks, Mr Tay highlighted the historical development of the ASEAN Smart City Network
                        (ASCN), highlighting the enabling environment in Singapore as a Smart Nation.
                    </p>
                    <p>
                        Dr Anbumozhi, the main speaker, explained there are no clear guidelines for developing smart cities; each
                        city has its own definition. Transforming cities into smart cities is key to providing ‘intelligent
                        solutions’ to the challenges of urbanization, said Dr Anbumozhi. Generally speaking, a ‘smart city’ can be
                        understood as a city that has improved facilities and services in order to deal with urbanization
                        challenges
                        including energy, mobility, ICT, waste and wastewater management, and emissions reduction.
                    </p>
                    <p>
                        The ASEAN Smart Cities Network (ASCN) includes six components common to smart city development: (a) smart
                        governance, (b) smart living, (c) smart economy, (d) smart people, (e) smart environment, and (f) smart
                        mobility. ICT, data management and the Internet of Things (IoT) also play essential roles to enable a
                        smart
                        city environment. Learning from Singapore, the cities of Chiang Mai, Johor Bahru and Jakarta, have
                        employed
                        a common but differentiated technology roadmap to achieve smart city goals.
                    </p>
                    <p>
                        In terms of financing smart cities, Dr Anbumozhi suggested that collaboration amongst stakeholders is
                        crucial. City governments have budget allocations for city improvement that can be used for smart city
                        development. Meanwhile, community funding, such as crowdfunding may be tapped to fund development. However
                        city governments need to coordinate policies and decisions with other levels of national governments. In
                        addition to that, Mr Tay suggested that Public-Private Partnership (PPP) should be considered as well.
                    </p>
                    <p>
                        The preliminary conclusion of this research study covers three points: there is an opportunity in ASEAN
                        for
                        a paradigm shift towards sustainable cities, technology development is critical for infrastructure and
                        service delivery, and incorporation of digital intelligence, digital inclusion and innovation networks
                        into
                        strategic planning of ASEAN Smart cities models is important.
                    </p>
