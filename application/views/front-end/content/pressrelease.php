                    <p>
                        With rapidly rising demand for energy, Southeast Asian nations need to enhance energy security, including
                        fuel supply stability and affordability. Southeast Asian nations will be increasingly dependent on
                        imported oil and natural gas, making them more vulnerable to possible supply disruptions.According to
                        ERIA’s Energy Outlook and Energy Saving Potential in East Asia 2019, demand for fossil fuels will grow by
                        79% between 2015 and 2040. ASEAN has set ambitious goals to increase the use of renewable energy, yet
                        strong demand for fossil fuels could contradict the decarbonization envisaged under the Paris Agreement.
                        Furthermore, the growing frequency of extreme weather could damage energy infrastructure.
                    </p>
                    <p>
                        The Economic Research Institute for ASEAN and East Asia (ERIA) together with US-ASEAN Connect and the US
                        Mission to ASEAN, hosted a discussion on with Ambassador Virginia E. Palmer, Principal Deputy Assistant
                        Secretary in the U.S. State Department’s Bureau of Energy Resources.
                    </p>
                    <p>
                        In her keynote speech, Ambassador Palmer stressed that the U.S. and ASEAN countries shared a common
                        definition of energy security, namely that countries needed to develop the sources, suppliers and routes
                        they determined were best for their economy, free from outside interference. She noted three key elements
                        of the Unites States Asia Enhancing Development and Growth Through Energy (Asia EDGE) program: levelling
                        the playing field for energy investment; encouraging open energy trade and markets; and international
                        standard regulatory regimes to facilitate trade and investment while ensuring safety and sustainability.
                    </p>
                    <p>
                        On the other hand, the main challenges ASEAN is facing is to respond to an estimated 40 percent increase
                        in demand in ASEAN over the next 20 years, to integrate and develop its energy market, and to transition
                        to clean energy. Acknowledging ASEAN’s dependence on fossil fuel, Ambassador Palmer said, ‘Even as some
                        countries in the region face energy shortages and crises, we believe that there are US$2.5 trillion worth
                        of oil and gas resources in the that are unexploited..’
